package pruebas;

/**
 *
 * @author Alejandro
 */
public class Senia {
    
    private int id_palabra;
    private String palabra;
    private String morfema;
    private String imagen;
    
    public Senia(){
    }

    public int getId_palabra() {
        return id_palabra;
    }

    public void setId_palabra(int id_palabra) {
        this.id_palabra = id_palabra;
    }

    public String getPalabra() {
        return palabra;
    }

    public void setPalabra(String palabra) {
        this.palabra = palabra;
    }

    public String getMorfema() {
        return morfema;
    }

    public void setMorfema(String morfema) {
        this.morfema = morfema;
    }

    public String getImagenes() {
        return imagen;
    }

    public void setImagen(String imagenes) {
        this.imagen = imagenes;
    }

}
